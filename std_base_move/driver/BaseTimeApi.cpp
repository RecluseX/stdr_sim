void BaseTimeSleep(U32 u32SleepMS)
{
    usleep(u32SleepMS * 1000);
}

/***************************************************************
Description:   get system tick, (ms unit)
Input:
Output:
Return
****************************************************************/
S64 BaseGetTimeTick()
{
    S64 s32TickNow = 0;

    struct timespec ts;
    clock_gettime(CLOCK_REALTIME, &ts);//CLOCK_MONOTONIC
    s32TickNow = ts.tv_sec * 1000 + ts.tv_nsec / 1000000;

    return s32TickNow;
}




